While most players wear studded football boots ("soccer shoes"[3][4] or "cleats"[4] in North America), the Laws do not specify that these are required.[1] Shirts must have sleeves (both short and long sleeves are accepted), and goalkeepers must wear shirts which are easily distinguishable from all other players and the match officials. Thermal undershorts may be worn, but must be the same colour as the shorts themselves. Shin pads must be covered entirely by the stockings, be made of rubber, plastic or a similar material, and "provide a reasonable degree of protection".[1] The only other restriction on equipment defined is the requirement that a player must not use equipment or wear anything deemed dangerous to himself or another player.[1]

It is normal for individual competitions to specify that all outfield players on a team must wear the same colours, though the Law states only "The two teams must wear colours that distinguish them from each other and also the referee and the assistant referees".[1] In the event of a match between teams who would normally wear identical or similar colours the away team must change to a different colour.[5] Because of this requirement a team's second-choice is often referred to as its "away kit" or "away colours", although it is not unknown, especially at international level, for teams to opt to wear their away colours even when not required to by a clash of colours, or to wear them at home. The England national team sometimes plays in red shirts even when it is not required, as this was the strip worn when the team won the 1966 FIFA World Cup.[6] In some cases both teams have been forced to wear their second choice away kits; such as the match between Netherlands and Brazil in the 1974 FIFA World Cup where they wore white and dark blue rather than their first choice of orange and yellow, respectively; and the match between Netherlands and Spain in the 2014 FIFA World Cup where they wore dark blue and white rather than their home colors of orange and red, respectively.

