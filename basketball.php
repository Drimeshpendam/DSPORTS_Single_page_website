          	<p style="text-align: justify;">
Games are played in four quarters of 10 (FIBA)[37] or 12 minutes (NBA).[38] College men's games use two 20-minute halves,[39] college women's games use 10-minute quarters,[40] and most United States high school varsity games use 8-minute quarters; however, this varies from state to state.[41][42] 15 minutes are allowed for a half-time break under FIBA, NBA, and NCAA rules[39][43][44] and 10 minutes in United States high schools.[41] Overtime periods are five minutes in length[39][45][46] except for high school, which is four minutes in length.[41] Teams exchange baskets for the second half. The time allowed is actual playing time; the clock is stopped while the play is not active. Therefore, games generally take much longer to complete than the allotted game time, typically about two hours.

Five players from each team may be on the court at one time.[47][48][49][50] Substitutions are unlimited but can only be done when play is stopped. Teams also have a coach, who oversees the development and strategies of the team, and other team personnel such as assistant coaches, managers, statisticians, doctors and trainers.

            </p> 
                      
          </div>
          <div class="col-lg-6 pt-4 pt-lg-0">
            <p style="text-align: justify;">
For both men's and women's teams, a standard uniform consists of a pair of shorts and a jersey with a clearly visible number, unique within the team, printed on both the front and back. Players wear high-top sneakers that provide extra ankle support. Typically, team names, players' names and, outside of North America, sponsors are printed on the uniforms.

A limited number of time-outs, clock stoppages requested by a coach (or sometimes mandated in the NBA) for a short meeting with the players, are allowed. They generally last no longer than one minute (100 seconds in the NBA) unless, for televised games, a commercial break is needed.

The game is controlled by the officials consisting of the referee (referred to as crew chief in the NBA), one or two umpires (referred to as referees in the NBA) and the table officials. For college, the NBA, and many high schools, there are a total of three referees on the court. The table officials are responsible for keeping track of each team's scoring, timekeeping, individual and team fouls, player substitutions, team possession arrow, and the shot clock.

